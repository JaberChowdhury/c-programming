#include <stdio.h>

int main() {
  // Arithmetic operator
  const int x = 1090;
  const int y = 8277;
  int z = 86;

  printf("Addition %d\n", x + y);
  printf("Subtraction %d\n", x - y);
  printf("Multiplication %d\n", x * y);
  printf("Divition %d\n", x / y);
  printf("Modulas %d\n", x % y);
  printf("Increment %d\n", ++z);
  printf("Decrement %d\n", --z);

  // Assignment operator

  return 0;
}
